# The variable num1 has the value 99.23 which is a decimal number 
# ie. float data type.
num1 = 99.23

# The variable num2 has the value 23 which is a whole number 
# ie. an integer data type.
num2 = 23

#The variable num3 has the value 150 which is whole number 
# ie. an integer data type.
num3 = 150

#The variable string1 holds the value 100 as a string data type.
string1 = "100"

print(num1)
print(num2)
print(num3) 
print(string1)

# Below the variable num1_int is given the data type integer (int) to
# convert the float data type 99.23 to an integer.
num1_int = int(num1)

# Below the variable num2_float is given the data type float to convert
# the integer 23 held in Variable num2 into a float data type.
num2_float = float(num2)

# Below the variable num3_string is given the data type string to convert
# the integer 150 held in Variable num 3 into a string data type.
num3_string = str(num3)

# Below the variable string1_int is given the data type int to convert
# the string "100" held in the Variable string1 into an integer.
string1_int = int(string1)

print(num1_int)
print(num2_float)
print(num3_string)
print(string1_int)


